function getNormalLevel(clearedCount) {
  if (clearedCount < NORMAL_LEVELS.length) {
    return NORMAL_LEVELS[clearedCount];
  }
  return null;
}

function getMechTutorial(mech, index) {
  var list = MECH_TUTORIALS[mech];
  if (!list || index >= list.length) return null;
  return list[index];
}

function getMechTutorialCount(mech) {
  var list = MECH_TUTORIALS[mech];
  return list ? list.length : 0;
}

function getMechLabel(mech) {
  return MECH_LABELS[mech] || mech;
}

